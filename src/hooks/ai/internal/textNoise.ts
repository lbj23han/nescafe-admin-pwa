export function removeRelativeDateNoise(text: string) {
  let t = text ?? "";
  if (!t.trim()) return t;

  // 1) 단일 토큰: 오늘/내일/모레/어제
  // 문장 중간에서도 제거될 수 있게 공백/쉼표 경계 기반
  t = t.replace(/(^|[\s,])(오늘|내일|모레|어제)(?=$|[\s,])/g, " ");

  // 2) 이번주/다음주/다다음주 + 요일(+요일)
  // 예: "이번주 토요일", "다음주수요일", "다다음주 금"
  t = t.replace(
    /(이번주|다음주|다다음주)\s*(월|화|수|목|금|토|일)(?:요일)?/g,
    " "
  );

  // 3) 이번달/다음달/다다음달 + N일
  // 예: "다음달 3일", "이번달31일"
  t = t.replace(/(이번달|다음달|다다음달)\s*\d{1,2}\s*일/g, " ");

  return t;
}
